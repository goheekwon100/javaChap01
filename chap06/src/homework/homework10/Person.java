package homework.homework10;

public class Person {
	public String name;
	public int age;
	
	

	public Person() {
		
	}
	
	//getter setter
	public String getName() {
		return name;
	}

	public int getAge() {
		return age;
	}
	
	
	public void setName(String name) {
		this.name = name;
	}
	public void setAge(int age) {
		this.age = age;
	}
}
